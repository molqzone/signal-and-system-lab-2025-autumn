n = -2:10;                         % 定义范围，包含一部分负区间以显示 u(n) 的效果
u = n >= 0;                        % 生成单位阶跃序列 u(n)
x = (1/2).^(n-1) .* u;             % 计算 x(n)，注意使用点运算 .^ 和 .*

figure;                            % 创建新窗口
stem(n, x, 'filled', 'LineWidth', 1.5); % 绘制火柴棒图
xlabel('n');                       % 横轴标签
ylabel('x(n)');                    % 纵轴标签
title('x(n) = (1/2)^{n-1} u(n)');  % 标题
grid on;                           % 显示网格

% 在坐标轴上添加辅助线
line([min(n) max(n)], [0 0], 'Color', 'k');