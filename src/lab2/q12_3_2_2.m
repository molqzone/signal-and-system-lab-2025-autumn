n = 0:40;                         % 定义自变量范围（显示两个周期）
x = cos(n * pi / 10 - pi / 5);    % 计算序列值值

figure;                           % 创建新窗口
stem(n, x, 'filled');             % 绘制火柴棒图
xlabel('n');                      % 横轴标签
ylabel('x(n)');                   % 纵轴标签
title('x(n) = cos(n\pi/10 - \pi/5)'); % 标题
grid on;                          % 显示网格