n = 0:15;                         % 定义自变量范围（指数增长较快，范围不宜过大）
x = (3/2).^n .* sin(n * pi / 5);  % 计算序列值（注意使用点运算 .^ 和 .*）

figure;                           % 创建新窗口
stem(n, x, 'filled');             % 绘制火柴棒图
xlabel('n');                      % 横轴标签
ylabel('x(n)');                   % 纵轴标签
title('x(n) = (3/2)^n sin(n\pi/5)'); % 标题
grid on;                          % 显示网格