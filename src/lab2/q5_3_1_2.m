% 题目：已知系统的微分方程和激励信号 f(t) 如下，试用 MATLAB 命令
%       绘出系统零状态响应的时域仿真波形图：
%       (2) y''(t) + 4y'(t) + 4y(t) = f'(t) + 3f(t), f(t) = e^(-t)u(t)
% =============================================================================

num = [1 3];
den = [1 4 4];
sys = tf(num, den);
t = 0:0.01:8;
f = exp(-t);
[y, t_out] = lsim(sys, f, t);

figure;
plot(t_out, y, 'LineWidth', 1.5);
title('zero-state y(t)');
xlabel('t(s)');
ylabel('y(t)');
grid on;