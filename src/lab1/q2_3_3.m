% 题目：利用 MATLAB 命令产生幅度为 1、周期为 1、占空比为 0.5 的
%       一个周期矩形脉冲信号。
% =============================================================================
T = 1;              % 周期
A = 1;              % 幅度
duty_cycle = 50;    % 占空比 50%
t = -1 : 0.001 : 3; % 定义观察范围（涵盖多个周期）

% 使用 square 函数产生 [-1, 1] 的方波，再平移缩放到 [0, 1]
y = (A/2) * (square(2 * pi / T * t, duty_cycle) + 1);

figure('Name', '周期矩形脉冲');
plot(t, y, 'LineWidth', 2);
axis([-1, 3, -0.2, 1.2]); % 设置坐标轴
grid on;
xlabel('t'); ylabel('f(t)');
title('周期矩形脉冲 (A=1, T=1, Duty=0.5)');