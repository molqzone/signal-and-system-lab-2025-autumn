% 题目：利用 MATLAB 命令画出下列连续信号的波形图：
%       (4) f(t) = [1 + cos(πt)][u(t) - u(t-2)]
% =============================================================================
t = -1 : 0.01 : 3;               % 定义时间范围
ut = (t >= 0);                   % u(t)
ut_2 = (t >= 2);                 % u(t-2)
window = ut - ut_2;              % 矩形窗 [0, 2]
y = (1 + cos(pi * t)) .* window; % 计算信号

figure;
plot(t, y, 'LineWidth', 1.5);
grid on;
axis([-1, 3, -0.5, 2.5]);
xlabel('t'); ylabel('f(t)');
title('f(t) = [1 + cos(\pi t)][u(t) - u(t-2)]');