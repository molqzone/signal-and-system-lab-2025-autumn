t = -5:0.01:5;

% 2. 计算信号函数值
% 注意：sinc 函数在 MATLAB 中是内置的，cos 函数中的 pi 是常数 π
% 两个函数相乘时需要使用点乘运算符 '.*'
x2 = sinc(t) .* cos(10*pi*t);

% 3. 绘制波形图
figure;
plot(t, x2, 'LineWidth', 1.5);
grid on; % 显示网格线
title('x_2(t) = sinc(t)cos(10\pi t)');
xlabel('t');
ylabel('x_2(t)');