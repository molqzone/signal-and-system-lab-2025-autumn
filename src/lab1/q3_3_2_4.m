% 绘制信号 f(4-t/2) 的波形

% 1. 定义原始信号 f(t) 的函数句柄
f = @(t) (1*t + 1).*(t >= -2 & t < -1) + ...
    (1).*(t >= -1 & t < 0) + ...
    (2).*(t >= 0 & t < 1) + ...
    (-t + 2).*(t >= 1 & t <= 2);

% 2. 定义时间范围
% 由于 f(t) 在 [-2, 2] 有值，令 -2 <= 4-t/2 <= 2 可知有效范围在 [4, 12]
t = 0:0.01:14;

% 3. 计算 f(4-t/2)
y = f(4 - t/2);

% 4. 绘图
figure('Name', 'Problem 2-(4)');
plot(t, y, 'LineWidth', 2);
grid on;
axis([0 14 -1.5 2.5]);
xlabel('t');
ylabel('f(4-t/2)');
title('信号 f(4-t/2) 的波形');