% 题目：已知连续时间信号 f(t) 的波形如图 3-6 所示，
%       试用 MATLAB 命令画出下列信号的波形图：
%       (2) f(2-t)
% =============================================================================
% 绘制信号 f(2-t) 的波形

% 1. 定义原始信号 f(t) 的函数句柄
f = @(t) (1*t + 1).*(t >= -2 & t < -1) + ...
    (1).*(t >= -1 & t < 0) + ...
    (2).*(t >= 0 & t < 1) + ...
    (-t + 2).*(t >= 1 & t <= 2);

% 2. 定义时间范围
% 由于 f(t) 在 [-2, 2] 有值，f(2-t) 的有效范围在 [0, 4] 左右
t = -2:0.01:6;

% 3. 计算 f(2-t)
y = f(2 - t);

% 4. 绘图
figure('Name', 'Problem 2-(2)');
plot(t, y, 'LineWidth', 2);
grid on;
axis([-2 6 -1.5 2.5]);
xlabel('t');
ylabel('f(2-t)');
title('信号 f(2-t) 的波形');