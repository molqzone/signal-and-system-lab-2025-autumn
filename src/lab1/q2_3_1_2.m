t = -2 : 0.01 : 6;               % 定义时间范围
ut = (t >= 0);                   % 生成单位阶跃函数 u(t)
y = (2 - exp(-t)) .* ut;         % 计算信号（点乘运算）

figure;
plot(t, y, 'LineWidth', 1.5);
grid on;
axis([-2, 6, -0.5, 2.5]);
xlabel('t'); ylabel('f(t)');
title('f(t) = (2 - e^{-t})u(t)');