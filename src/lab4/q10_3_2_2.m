% 题目 2(2)
num = [3];
% 分母展开: (s+5)(s+2) = s^2 + 7s + 10
den = conv([1 5], [1 2]);

[r, p, k] = residue(num, den);

fprintf('部分分式展开结果：\n');
for i = 1:length(r)
    fprintf('  %.1f / (s - %.1f)\n', r(i), p(i));
end
fprintf('\n对应的时域解为: %.1f*exp(%.1ft) + %.1f*exp(%.1ft)\n', r(1), p(1), r(2), p(2));