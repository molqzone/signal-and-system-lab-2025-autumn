% 题目：已知某线性时不变系统的系统函数为：
%       H(s) = (4s² + 4s + 4)/(s³ + 3s² + 2s)
%       利用 MATLAB 的拉普拉斯变换法求系统的单位阶跃响应。
% =============================================================================

% 题目 3
num = [4 4 4];
den = [1 3 2 0]; % s^3 + 3s^2 + 2s

% 创建传递函数对象
sys = tf(num, den);

% 1. 获取数值响应数据
[y, t] = step(sys);

% 2. 绘制波形图（最直观的方法）
figure;
step(sys);
title('系统单位阶跃响应 (数值解)');
grid on;

% 3. 如果需要解析式，依然建议使用 residue
[r, p, k] = residue(num, den);
disp('系统的部分分式展开系数（可据此直接写出解析式）：');
disp(table(r, p));